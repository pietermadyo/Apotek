    private void selectedDataObat() 
    {
      
       int temp = jTable2.getSelectedRow();
       apa=jTable2.getSelectedRow();
      Globaltemp=Integer.parseInt(jTable2.getValueAt(temp,0).toString());
       
       txtNama.setText(jTable2.getValueAt(temp,1).toString());
       txtharga.setText(jTable2.getValueAt(temp,5).toString());      
        SpinnerStok.setValue(Integer.parseInt(jTable2.getValueAt(temp,3).toString()));
        cmbkategori.setSelectedItem(jTable2.getValueAt(temp,2).toString());
        cmbstatus.setSelectedItem(jTable2.getValueAt(temp,4).toString());
        
    }
    
    
    public void clear()
    {
        
        txtNama.setText("");
        txtharga.setText("");
        txtcari.setText("");
        SpinnerStok.setValue(0);
        cmbkategori.setSelectedIndex(0);
        cmbstatus.setSelectedIndex(0);
        showObat();
    }
    public void showObat()
    {
        
        jTable2.setModel(oc.displayObat());
        jTable2.getColumnModel().getColumn(0).setMaxWidth(60);
    } 
    
