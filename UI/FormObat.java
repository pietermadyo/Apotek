    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        try
        {
            cekKosong();
            Obat O = new Obat
        (Globaltemp,
                cmbkategori.getSelectedItem().toString(),
                txtNama.getText(),
                Integer.parseInt(SpinnerStok.getValue().toString()),
                cmbstatus.getSelectedItem().toString(),
                Double.parseDouble(txtharga.getText()));     
        C.InsertData(O);
        clear();
       JOptionPane.showMessageDialog(this, "Data Berhasil disimpan");
       showObat();
            
        }catch(cektextkosongException e)
        {
            JOptionPane.showMessageDialog(this,e.peringatan());
        }
         
    }//GEN-LAST:event_btnTambahActionPerformed

    private void cekKosong() throws cektextkosongException
    {
       if(txtNama.getText().equals("") || txtharga.getText().equals(""))
       {
           throw new cektextkosongException();
       }
           
    }
    
